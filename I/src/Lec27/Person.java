package Lec27;

public class Person {
	private String name;
	private int age;
	private String address;
	private long mobileNumber;

	public Person() {

	}

	public Person(String name, int age, String address, long mobileNumber) {
		this.name = name;
		this.age = age;
		this.address = address;
		this.mobileNumber = mobileNumber;
	}

}
